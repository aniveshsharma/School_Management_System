    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        // TODO add your handling code here:
        
        String r = rollno.getText();
        String nme = name.getText();
        String fname = fathername.getText();
        String mname = mothername.getText();
        String birth = dob.getText();
        String Adhar_no = adharno.getText();
        String pnum = phoneno.getText();
        String mail = email.getText();
        String cls = cl.getText();
        String Add = address.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn;
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","2202");
            String sql;
            sql = "UPDATE `student` SET `Name_` ='"+nme+"', `Father_name` = '"+fname+"', `Mother_name` = '"+mname+"', `DOB` = '"+birth+"', `Adhar_no` = '"+Adhar_no+"', `Phone_no` = '"+pnum+"', `E_mail` = '"+mail+"', `Class` = '"+cls+"', `Address` = '"+Add+"' WHERE `ID` = '"+r+"'";

            PreparedStatement ptstmt=conn.prepareStatement(sql);
            ptstmt.execute();
            
            
            JOptionPane.showMessageDialog(null, "Record has been Updated Sucessfully!");
        }
         catch (HeadlessException | ClassNotFoundException | SQLException e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_UpdateActionPerformed

