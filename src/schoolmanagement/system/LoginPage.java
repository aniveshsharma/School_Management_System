    private void LoginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginbuttonActionPerformed
        // TODO add your handling code here:
        String ui=User_id.getText();
        String p = Password.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn;
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/sms","root","2202");
            Statement st=(Statement)conn.createStatement();
            String sql = "select * from user_login";
            
             
            ResultSet rs;
            rs = st.executeQuery(sql);
            while(rs.next()){
            String user_id = rs.getString("user_id");
            String password = rs.getString("password_");
            
            
            
            
            if(ui.equals(user_id) && p.equals(password)){
            new Welcome().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(this , "User_id or password is incorrect!");
            }
            }
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null,"Error while establishing connection!");
        }
    }//GEN-LAST:event_LoginbuttonActionPerformed

